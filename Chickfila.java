public class Chickfila {
    // Properties
    int waitTime;
    double rating;
    double avgPrice;
    double distance;
    String hoursOpen;

    // Constructor
    public Chickfila() {}

    public Chickfila(int waitTime, double rating, double avgPrice, double distance, String hoursOpen) {
        this.waitTime = waitTime;
        this.rating = rating;
        this.avgPrice = avgPrice;
        this.distance = distance;
        this.hoursOpen = hoursOpen;
    }


}
